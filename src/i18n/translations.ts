export function resolveCategoryLabel(
  id: string,
  locale: AppLocale,
  custom: CustomCategory[],
): string {
  const row = custom.find(c => c.id === id);
  if (row) {
    return row.label;
  }
  return BUILTIN_CATEGORY_LABELS[locale]?.[id] ?? BUILTIN_CATEGORY_LABELS.en[id] ?? id;
}

